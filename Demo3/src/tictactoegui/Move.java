/**
 * Class represents a move for the tictactoegui. The class specifies a move
 * requires a specific row and column set as an integer.
 * 
 */ 

package tictactoegui;

public class Move {
    int row;
    int column;
    public Move(int aRow, int aColumn) {
        row = aRow;
        column = aColumn;
    }
}
